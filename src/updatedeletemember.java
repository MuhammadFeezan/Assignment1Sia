    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String id=jTextField1.getText();
        String name=jTextField2.getText();
        String mobilenumber=jTextField3.getText();
        String fathername=jTextField4.getText();
        String email=jTextField7.getText();
        String accountnumber=jTextField5.getText();
        String age=jTextField6.getText();
        String accounttopay=jTextField8.getText();
        try
        {
            Connection con=connectionprovider.getCon();
            PreparedStatement ps=con.prepareStatement("update mmembere set name=?,mobilenumber=?,fathername=?,email=?,accountnumber=?,age=?,accounttopay=?where id=?");
            ps.setString(1,name);
            ps.setString(2,mobilenumber);
            ps.setString(3,fathername);
            ps.setString(4,email);
            ps.setString(5,accountnumber);
            ps.setString(6,age );
            ps.setString(7,accounttopay);
            ps.setString(8,id);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "succesfully updated");
            setVisible(false);
            new updatedeletemember().setVisible(true);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, age);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

